package org.simbiosis.system.model;

public class RefProvCity {
	long id;
	int level;
	String name;
	String code;
	long prov;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public long getProv() {
		return prov;
	}

	public void setProv(long prov) {
		this.prov = prov;
	}

}
